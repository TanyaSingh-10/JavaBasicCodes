public class Primitives {
    public static void main(String[] args) {
        
        int rollno = 64;
        System.out.println(rollno);

        // string is not primitive datatype
        String name = "kunal";
        System.out.println(name);

        char letter = 'r';
        System.out.println(letter);

        // float has 4 bytes of space
        float marks = 98.67f;
        System.out.println(marks);
        double largeDecimalNumbers = 46789654.4567;

        // long has 8 bytes of space
        long largerInteger = 1234567890987654567L;

        // boolean datatypes either give true or false value
        boolean check = true;
        System.out.println(check);
    
    }
}
